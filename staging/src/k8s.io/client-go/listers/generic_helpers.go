package listers

import (
	"k8s.io/apimachinery/pkg/api/errors"
	"k8s.io/apimachinery/pkg/labels"
	"k8s.io/apimachinery/pkg/runtime"
	"k8s.io/apimachinery/pkg/runtime/schema"
	"k8s.io/client-go/tools/cache"
)

// ResourceIndexer wraps an indexer and resource for a given type.
// This is intended for use by listers (generated by lister-gen) only.
type ResourceIndexer[T runtime.Object] struct {
	indexer  cache.Indexer
	resource schema.GroupResource
}

// NamespacedResourceIndexer wraps an indexer, resource, and namespace for a given type.
// This is intended for use by listers (generated by lister-gen) only.
type NamespacedResourceIndexer[T runtime.Object] struct {
	ResourceIndexer[T]
	namespace string
}

// New returns a new instance of a lister (resource indexer) wrapping the given indexer and resource for the specified type.
// This is intended for use by listers (generated by lister-gen) only.
func New[T runtime.Object](indexer cache.Indexer, resource schema.GroupResource) ResourceIndexer[T] {
	return ResourceIndexer[T]{indexer: indexer, resource: resource}
}

// NewNamespaced returns a new instance of a namespaced lister (resource indexer) wrapping the given parent and namespace for the specified type.
// This is intended for use by listers (generated by lister-gen) only.
func NewNamespaced[T runtime.Object](parent ResourceIndexer[T], namespace string) NamespacedResourceIndexer[T] {
	return NamespacedResourceIndexer[T]{parent, namespace}
}

// List lists all resources in the indexer matching the given selector.
func (l ResourceIndexer[T]) List(selector labels.Selector) (ret []T, err error) {
	err = cache.ListAll(l.indexer, selector, func(m interface{}) {
		ret = append(ret, m.(T))
	})
	return ret, err
}

// Get retrieves the resource from the index for a given name.
func (l ResourceIndexer[T]) Get(name string) (T, error) {
	obj, exists, err := l.indexer.GetByKey(name)
	if err != nil {
		return *new(T), err
	}
	if !exists {
		return *new(T), errors.NewNotFound(l.resource, name)
	}
	return obj.(T), nil
}

// List lists all resources in the indexer and namespace matching the given selector.
func (l NamespacedResourceIndexer[T]) List(selector labels.Selector) (ret []T, err error) {
	err = cache.ListAllByNamespace(l.indexer, l.namespace, selector, func(m interface{}) {
		ret = append(ret, m.(T))
	})
	return ret, err
}

// Get retrieves the resource from the index for a given name in the lister's namespace.
func (l NamespacedResourceIndexer[T]) Get(name string) (T, error) {
	obj, exists, err := l.indexer.GetByKey(l.namespace + "/" + name)
	if err != nil {
		return *new(T), err
	}
	if !exists {
		return *new(T), errors.NewNotFound(l.resource, name)
	}
	return obj.(T), nil
}
