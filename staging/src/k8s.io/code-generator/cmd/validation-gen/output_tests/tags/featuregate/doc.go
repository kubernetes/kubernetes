// +k8s:validation-gen=TypeMeta
// +k8s:validation-gen-scheme-registry=k8s.io/code-generator/cmd/validation-gen/testscheme.Scheme

// This is a test package.
// +k8s:validation-gen-nolint
package featuregate

import "k8s.io/code-generator/cmd/validation-gen/testscheme"

var localSchemeBuilder = testscheme.New()

// Struct tests +k8s:featureGate combined with +k8s:optional and +default.
// When the gate is enabled, optional+default makes the field effectively required.
// When the gate is disabled, the field is forbidden (auto-generated by +k8s:featureGate).
type Struct struct {
	TypeMeta int

	// +k8s:featureGate=MyGate
	// +k8s:optional
	// +default="Pod"
	StringPtrField *string `json:"stringPtrField"`
}

// MultiGateStruct tests +k8s:featureGate with multiple gates.
// All gates must be enabled for the field to be allowed.
type MultiGateStruct struct {
	TypeMeta int

	// +k8s:featureGate=GateA
	// +k8s:featureGate=GateB
	// +k8s:optional
	// +default="default"
	MultiGatedField *string `json:"multiGatedField"`
}

// TypesStruct tests +k8s:featureGate with different field types.
type TypesStruct struct {
	TypeMeta int

	// Slice field gated behind a feature gate.
	// +k8s:featureGate=SliceGate
	// +k8s:optional
	SliceField []string `json:"sliceField"`

	// Map field gated behind a feature gate.
	// +k8s:featureGate=MapGate
	// +k8s:optional
	MapField map[string]string `json:"mapField"`

	// Non-pointer primitive field gated behind a feature gate.
	// +k8s:featureGate=ValueGate
	// +k8s:optional
	ValueField string `json:"valueField"`

	// Pointer-to-struct field gated behind a feature gate.
	// +k8s:featureGate=StructPtrGate
	// +k8s:optional
	StructPtrField *InnerStruct `json:"structPtrField"`
}

// InnerStruct is a simple struct used as a field type.
// +k8s:validation-gen=TypeMeta
type InnerStruct struct {
	TypeMeta int

	// +k8s:minLength=1
	Name string `json:"name"`
}
