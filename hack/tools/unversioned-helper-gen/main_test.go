package main

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestGenerator(t *testing.T) {
	tests := []struct {
		name           string
		inputContent   string
		expectedOutput string
		expectErr      string
	}{
		{
			name: "basic translation",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	v1 "k8s.io/api/core/v1"
)

// DummyStruct is a dummy struct.
type DummyStruct struct {
	Pod *v1.Pod
}
`,
			expectedOutput: `// Code generated by unversioned-helper-gen. DO NOT EDIT.
// Source: input/helper.go

package dummy

import (
	core "k8s.io/kubernetes/pkg/apis/core"
)

// DummyStruct is a dummy struct.
type DummyStruct struct {
	Pod *core.Pod
}
`,
		},
		{
			name: "redirection with explicit alias",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
	resourcehelper "k8s.io/component-helpers/resource"
)

func Foo() {
	resourcehelper.IsPodLevelResourcesSet(nil)
}
`,
			expectedOutput: `// Code generated by unversioned-helper-gen. DO NOT EDIT.
// Source: input/helper.go

package dummy

import (
	resourcehelper "k8s.io/kubernetes/pkg/apis/core/helper"
)

func Foo() {
	resourcehelper.IsPodLevelResourcesSet(nil)
}
`,
		},
		{
			name: "redirection with implicit alias",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
	"k8s.io/component-helpers/resource"
)

func Foo() {
	resource.IsPodLevelResourcesSet(nil)
}
`,
			expectedOutput: `// Code generated by unversioned-helper-gen. DO NOT EDIT.
// Source: input/helper.go

package dummy

import (
	resource "k8s.io/kubernetes/pkg/apis/core/helper"
)

func Foo() {
	resource.IsPodLevelResourcesSet(nil)
}
`,
		},
		{
			name: "redirection same line comment",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	resourcehelper "k8s.io/component-helpers/resource" // +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
)

func Foo() {
	resourcehelper.IsPodLevelResourcesSet(nil)
}
`,
			expectedOutput: `// Code generated by unversioned-helper-gen. DO NOT EDIT.
// Source: input/helper.go

package dummy

import (
	resourcehelper "k8s.io/kubernetes/pkg/apis/core/helper"
)

func Foo() {
	resourcehelper.IsPodLevelResourcesSet(nil)
}
`,
		},
		{
			name: "missing gen tag",
			inputContent: `package dummy

import (
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
	resourcehelper "k8s.io/component-helpers/resource"
)
`,
			expectErr: "no +k8s:unversioned-gen tag found",
		},
		{
			name: "multiple gen tags",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy
// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy2

package dummy
`,
			expectErr: "multiple +k8s:unversioned-gen tags found",
		},
		{
			name: "multiple pkg tags on same import",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper2
	resourcehelper "k8s.io/component-helpers/resource"
)
`,
			expectErr: "multiple +k8s:unversioned-pkg tags found in comment group",
		},
		{
			name: "duplicate pkg tags across doc and comment",
			inputContent: `// +k8s:unversioned-gen=k8s.io/kubernetes/pkg/apis/dummy

package dummy

import (
	// +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
	resourcehelper "k8s.io/component-helpers/resource" // +k8s:unversioned-pkg=k8s.io/kubernetes/pkg/apis/core/helper
)
`,
			expectErr: "duplicate +k8s:unversioned-pkg tag on import",
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			tempDir := t.TempDir()

			// Create the expected output directory structure inside tempDir.
			outDir := filepath.Join(tempDir, "pkg", "apis", "dummy")
			if err := os.MkdirAll(outDir, 0755); err != nil {
				t.Fatalf("Failed to create output dir: %v", err)
			}

			// Create input file.
			inputDir := filepath.Join(tempDir, "input")
			if err := os.MkdirAll(inputDir, 0755); err != nil {
				t.Fatalf("Failed to create input dir: %v", err)
			}
			inputPath := filepath.Join(inputDir, "helper.go")

			if err := os.WriteFile(inputPath, []byte(tc.inputContent), 0644); err != nil {
				t.Fatalf("Failed to write input file: %v", err)
			}

			g := &generator{
				kubeRoot: tempDir,
			}

			_, err := g.processFile(inputPath)
			if tc.expectErr != "" {
				if err == nil {
					t.Fatalf("Expected error containing %q, got nil", tc.expectErr)
				}
				if !strings.Contains(err.Error(), tc.expectErr) {
					t.Fatalf("Expected error containing %q, got %q", tc.expectErr, err.Error())
				}
				return
			}

			if err != nil {
				t.Fatalf("processFile failed: %v", err)
			}

			// Verify output file.
			outputPath := filepath.Join(outDir, "helper_generated.go")
			if _, err := os.Stat(outputPath); os.IsNotExist(err) {
				t.Fatalf("Output file %s was not generated", outputPath)
			}

			outputContent, err := os.ReadFile(outputPath)
			if err != nil {
				t.Fatalf("Failed to read output file: %v", err)
			}

			if string(outputContent) != tc.expectedOutput {
				t.Errorf("Output content mismatch.\nExpected:\n%s\nGot:\n%s", tc.expectedOutput, string(outputContent))
			}
		})
	}
}
