package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"strings"

	"golang.org/x/tools/go/ast/astutil"
)

const Name = "internal-helper-gen"

func main() {
	var inputDir, outputDir string
	flag.StringVar(&inputDir, "input", "", "input directory path")
	flag.StringVar(&outputDir, "output", "", "output directory path")
	flag.Parse()

	if inputDir == "" || outputDir == "" {
		log.Fatalf("input and output must be provided")
	}

	pkgName := filepath.Base(outputDir)
	outputImportPath := getOutputImportPath(outputDir)

	files, err := os.ReadDir(inputDir)
	if err != nil {
		log.Fatalf("failed to read input directory: %v", err)
	}

	if err := os.MkdirAll(outputDir, 0755); err != nil {
		log.Fatalf("failed to create output directory: %v", err)
	}

	for _, file := range files {
		if file.IsDir() {
			continue
		}

		srcPath := filepath.Join(inputDir, file.Name())
		var dstPath string

		if strings.HasSuffix(file.Name(), ".go") {
			name := strings.TrimSuffix(file.Name(), ".go")
			if strings.HasSuffix(name, "_test") {
				dstPath = filepath.Join(outputDir, strings.TrimSuffix(name, "_test")+"_generated_test.go")
			} else {
				dstPath = filepath.Join(outputDir, name+"_generated.go")
			}
			translateFile(srcPath, dstPath, pkgName, outputImportPath)
		}
	}
}

func translateFile(input, output, pkg, outputImport string) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, input, nil, parser.ParseComments)
	if err != nil {
		log.Fatalf("failed to parse file %s: %v", input, err)
	}

	// Change package name
	f.Name.Name = pkg

	// If the input file imported the internal version, we need to drop the package alias.
	var selfAlias string

	// Update imports
	for _, imp := range f.Imports {
		path := strings.Trim(imp.Path.Value, `"`)

		// If the input file imported the internal version, make sure to drop the import.
		if outputImport != "" && path == outputImport {
			if imp.Name != nil {
				selfAlias = imp.Name.Name
			} else {
				selfAlias = pkg
			}
			continue
		}

		// Translate the versioned types to internal types.
		// TODO: extend this to work for types outside of core.
		if strings.HasPrefix(path, `k8s.io/api/`) {
			if path == `k8s.io/api/core/v1` {
				imp.Path.Value = `"k8s.io/kubernetes/pkg/apis/core"`
				imp.Name = &ast.Ident{Name: "core"}
			} else {
				log.Fatalf("file %s imports unsupported api %s. Only k8s.io/api/core/v1 is supported for now.", input, path)
			}
		}
	}

	// If the input file imported the internal version,
	if astutil.DeleteImport(fset, f, outputImport) {

	}

	// Update all references based on the alias map
	astutil.Apply(f, func(c *astutil.Cursor) bool {
		switch n := c.Node().(type) {
		case *ast.SelectorExpr:
			if ident, ok := n.X.(*ast.Ident); ok {
				// TODO: generalize this for types outside of core.
				if ident.Name == "v1" {
					ident.Name = "core"
				} else if selfAlias == ident.Name {
					// If the selector was the alias for the internal package, drop it.
					c.Replace(n.Sel)
				}
			}
		}
		return true
	}, nil)

	var buf bytes.Buffer
	if err := format.Node(&buf, fset, f); err != nil {
		log.Fatalf("failed to format node for %s: %v", input, err)
	}

	// Inject the header before the package declaration
	header := getGeneratedHeader(input)
	pkgDecl := "package " + pkg
	outStr := bytes.Replace(buf.Bytes(), []byte(pkgDecl), []byte(header+pkgDecl), 1)

	if err := os.WriteFile(output, outStr, 0644); err != nil {
		log.Fatalf("failed to write output %s: %v", output, err)
	}
	fmt.Printf("Generated %s from %s\n", output, input)
}

// getOutputImportPath gets the import path that refers to the output package.
func getOutputImportPath(outputDir string) string {
	absOutput, _ := filepath.Abs(outputDir)
	rel, err := filepath.Rel(getKubeRoot(), absOutput)
	if err != nil {
		return ""
	}
	return "k8s.io/kubernetes/" + filepath.ToSlash(rel)
}

func getGeneratedHeader(inputPath string) string {
	relPath := getRelativeSourcePath(inputPath)
	return fmt.Sprintf("// Code generated by %s. DO NOT EDIT.\n// Source: %s\n\n", Name, relPath)
}

func getRelativeSourcePath(inputPath string) string {
	absInput, _ := filepath.Abs(inputPath)
	relInput, err := filepath.Rel(getKubeRoot(), absInput)
	if err != nil {
		return inputPath
	}
	return relInput
}

func getKubeRoot() string {
	if kubeRoot := os.Getenv("KUBE_ROOT"); kubeRoot != "" {
		return kubeRoot
	}
	cwd, _ := os.Getwd()
	return cwd
}
